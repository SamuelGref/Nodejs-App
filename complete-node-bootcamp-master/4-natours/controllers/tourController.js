exports.getAlltours = async (req, res) => {
  try {
    const tours = await Tour.find();

    res.status(200).json({
      satutus: 'success',
      results: tours.length,
      data: {
        tours,
      },
    });
  } catch (err) {
    res.status(404).json({
      satutus: 'fail',
      message: err,
    });
  }
};

